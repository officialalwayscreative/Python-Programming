# Create a program that takes a day of the week (1-7) as input and prints the corresponding day name
Day = int (input("Enter a number :"))
if Day==0:
    print ("Sunday")
elif Day==1:
    print ("Monday")
elif Day==2:
    print ("Tuesday")
elif Day==3:
    print ("Wednesday")
elif Day==4:
    print ("Thursday")
elif Day==5:
    print ("Friday")
elif Day==6:
    print ("Saturday")
